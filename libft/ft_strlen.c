#include "libft.h"

size_t	ft_strlen(const char *s)
{
	size_t	i;

	i = 0;
	if (!s)
		return (0);
	while (s[i])
		i++;
	return (i);
}

int	main()
{
	char *p;
	p = "";

	printf("%zu\n", ft_strlen(p));
}
